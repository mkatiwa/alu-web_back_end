#!/usr/bin/env python3
"""
This module provides a function to calculate
    the floor of a floating-point number.
"""


import math


def floor(n: float) -> int:
    """
    Return the floor of the given floating-point number.

    Args:
        n (float): The floating-point number to floor.

    Returns:
        int: The floor value of the number.
    """
    return math.floor(n)
    