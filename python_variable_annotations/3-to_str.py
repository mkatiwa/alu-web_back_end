#!/usr/bin/env python3
"""
This module provides a function to convert a floating-point number
    to its string representation.
"""


def to_str(n: float) -> str:
    """
    Return the string representation of the given floating-point number.

    Args:
        n (float): The floating-point number to convert to a string.

    Returns:
        str: The string representation of the number.
    """
    return str(n)
    